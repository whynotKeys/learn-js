/*
산술 연산자 +, -, *, /, %, **
*/

console.log("1 + 4 * 5 = ", 1 + 4 * 5); // 21

console.log("(1 + 4) * 5 = ", (1 + 4) * 5); // 25

console.log("9 / 4 = ", 9 / 4); // 2.25

console.log("9 % 4 = ", 9 % 4); // 1

console.log("2 ** 3 = ", 2 ** 3); // 8

var age = 2 ** 3;

console.log("당신의 나이는 " + age + "살입니다.");

// `으로 감싼 문장 안에 ${}로 변수 불러오기
console.log(`당신의 나이는 ${age}살입니다.`);
